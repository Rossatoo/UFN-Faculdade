package Calculadora;

public class realizaConta {
	double a,b;
	
	public double somar() {
		double resultado = a + b;
		return resultado;
	}
	
	public double subtrair() {
		double resultado = a - b;
		return resultado;
	}
	
	public double multiplicar() {
		double resultado = a * b;
		return resultado;
	}
	
	public double dividir() {
		double resultado = a / b;
		return resultado;
	}
}
	